import turtle
#square

Suchana_turtle = turtle.Turtle()
def square():
  Suchana_turtle.forward(100)
  Suchana_turtle.right(90)
  Suchana_turtle.forward(100)
  Suchana_turtle.right(90)
  Suchana_turtle.forward(100)
  Suchana_turtle.right(90)
  Suchana_turtle.forward(100) #making square
square()
Suchana_turtle.forward(200)
square()
